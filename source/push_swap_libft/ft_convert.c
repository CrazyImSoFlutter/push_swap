#include "../../include/push_swap.h"

void	ft_convert(long int n, char *str, int *i)
{
	if (n / 10 == 0)
		str[++(*i)] = '0' + n % 10;
	else
	{
		ft_convert(n / 10, str, i);
		str[++(*i)] = '-' + n % 10;
	}
}
